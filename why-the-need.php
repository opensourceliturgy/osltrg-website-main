<p class = "first">
I have been asked on numerous occasions why go through
all the work to prepare a properly Open Source liturgy
when there already are already plenty of liturgies out
there.
The answer is that while there are several liturgies
out there that you can buy a copy of in a store (or
order a copy online) or even download from the Internet,
there is something missing from all of these.
Anyone can get ahold of a copy of those liturgies.
In some cases "anyone" means "anyone who can afford to pay"
- but others are free-of-charge, and therefore don't
have that limitation.
</p>

<p class = "more">
Why do I say "free-of-charge" rather than "free"?
This is not just a linguistic quirk, but
is actually critical to the reason why this project is
needed.
It is that while many of these resources are free
in the sense of one definition of the word "free",
there is another very critical definition of the word
"free" that these resources do not meet
- that being "free" in the sense of, well, actual freedom.
Of course, I needn't spend too many words here explaining
exactly what this means because the Free Software Foundation
has <a href = "<?php rmt_link_out('define_free_software'); ?>" target = "_blank">
a perfectly good document to explain what exactly this is</a>.
Instead, I shall devote the bulk of this article to explaining
why it is oh so important to have liturgical resources
that are free in this sense of the word.
</p>

<p class = "more">
Ultimately, the reasons why it is important to have
liturgical resources that are free in the sense of
actual freedom include both reasons that are purely
theological as well as reasons that are pastoral.
While the purely theological reasons are indeed
important and need to be discussed as well,
the focus of this document is on the pastoral
need for libre liturgical resources
("libre" meaning "free" in the sense of actual freedom).
</p>

<p class = "more">
In Jesus's ministry and teaching, one thing He
made absolutely clear
is the requirement that His followers stand up and
defend marginalized members of society.
Unfortunately, though, the main institutional
Catholic Church all too often betrays
this mission - not just by neglecting it,
but at times, by actively opposing it.
Many groups of people who are to varying
degrees marginalized by society,
people who should be able to find
a safe haven in the Church,
often find additional marginalization
instead.
The rhetoric dished out by too many people
in positions of ecclesiastical authority
is emotionally abusive of LGBT people
in the Church who as a result have to
make the dire choice
between submitting to even more emotional
abuse from the pulpit or not receiving
sacraments at all (unless the
authorities make that decision for them by
deciding to withhold communion
by invoking <a href = "https://en.wikipedia.org/wiki/Canon_915" target = "_blank"
>Canon 915</a>).
Furthermore, the hierarchy of the institutional
church has worked very hard on opposing
so many efforts for society to extend to
LGBT people basic rights and privileges that
other people take for granted.
As a matter of fact Bishop Robert Lynch
had quite a point,
after the recent tragedy in Orlando,
when he <a href = "<?php rmt_link_out('bishop-lynch-on-orlando'); ?>" target = "_blank">pointed out how the Church's breeding of contempt for LGBT people
is a factor behind anti-LGBT violence</a>.
</p>

<p class = "more">
The institutional Church's marginalization
of groups of people whom the Church aught
to be protecting is not limited to those
on the LGBT spectrum.
People suffering from celiac disease
and other severe gluten intolerances
are placed in a position where
they have to risk their health to
receive communion,
all due to a stubborn insistence that
"bread" be interpreted in a way that
requires that it be made out of wheat.
Even divorced and remarried Catholics are
generally barred from receiving communion
unless their previous marriages have been
annulled - which is in itself
a problematic requirement for many.
Oh -- and I haven't gotten started on
the hierarchy's persistent refusal to
take full responsibility for sexual
abuses of minors or to make prevention
of them a higher priority than less-relevant
criteria of whom they allow to serve as priests
- such as the candidate's gender,
natally-anatomical sex, sexual orientation,
marital status, and other such nonsense.
</p>

<p class = "more">
In some areas, Catholics who feel dejected
for these or other reasons (as there are too many
for me to list all of them here)
have a safe place to go where they can
receive sacraments free of any of these abuses.
They could go to a Dignity chapter, to
any of several Old Catholic Churches,
as well as a number of other options.
However, in other areas,
these options are either non-existent
or no more welcoming than the institutional Church,
and therefore not a proper alternative.
Dejected Catholics in those areas have three
options. They can either (a) continue going to
the institutional church (if even allowed that option)
and take more and more and more emotional abuse
from those who should be providing refuge
(b) walk away from the sacramental life altogether
or (c) organize for themselves alternative communities
in which to experience sacramental life.
</p>

<p class = "more">
As the first two of these three options generally
lead to increased risk of one losing faith
altogether - it is definitely preferable that
these dejected Catholics take the third option -
to organize safe communities for themselves.
However, in doing so, steep obstacles are faced.
Often, no presider ordained through the episcopal
pedigree of Apostolic Succession is
available to the community
- and the conditions that would need to be met
for bishops of the more inclusive Independent Catholic
churches to ordain anyone would take years (at best)
to meet -- which often is way too long for
people who already face plenty of persecution
in other areas of life to go without sacramental
support.
As for the option of celebrating without
anyone ordained through this pedigree - there
is the problem that many people will not
have enough confidence in the validity and
Catholicity of that approach to be served
in this manner.
Though the Dutch Dominicans did a great job
of explaining the theology behind the validity
of such extralineal celebration
<a href = "<?php rmt_link_out('church_and_ministry'); ?>" target = "_blank">in a document
that they directed at thriving communities
that were closing due to the shortage of priests</a>,
a far more concise explanation (that nobody has been
up to the task of writing) would be needed in
order to maximize the effect of an outreach
that is forced to resort to such means.
Even less available is a concise document
explaining the Catholicity of this approach.
(Even on this site, where having such a document
is on the to-do-list, that task has to take a number
as it is among several other things that also
need to be done.)
</p>

<p class = "more">
However, even if a group can overcome
these obstacles - one more issue that they
will have to deal with is finding liturgical
texts to use.
This is the obstacle that this project
is primarily aimed at helping people
overcome.
</p>

<p class = "more">
The first suggestion many people would think
of is using the same liturgical texts as
the Roman Catholic churches use.
However, the copyright protections of these
text pose a number of problems.
For starters, there is the danger that
if such a movement gains any traction,
the powers-that-be might get desperate enough
to shut them down that they resort to dirty
techniques such as the use of copyright law.
Furthermore, risk of getting into copyright
trouble increases if the group makes
any alterations to the text.
Even if such a group sees no
immediate need to make any alterations to the
text,
reserving the right to do so if necessary can
be very important.
For starters, there is the very real risk
that the very abusive attitudes people fled
from make their way into the texts themselves.
Also, lack of freedom to decide what
language does and doesn't get tweaked
puts these groups in a certain way once
again under the control of the very authorities
people fled from.
</p>

<p class = "more">
The only solution to this is to have
liturgical texts made available that
are available under a license that
makes them libre (which, again, means
"free" in the sense of actual freedom,
as opposed to price).
This way, the community can freely
distribute as many copies as are
needed, online or in print.
Also, a libre license assures that
(provided they cite all alterations)
they may made modifications to
these texts, and distribute
modified versions in the same
manners by which they are allowed
to distribute the as-provided versions.
</p>

<p class = "more">
There will be many obstacles that dejected
Catholics face in organizing safe communities
for themselves.
However, once libre liturgical texts are
made available, there will be one
obstacle fewer than there otherwise would be.
</p>

<p class = "more">
<i>God bless.</i>
</p>




